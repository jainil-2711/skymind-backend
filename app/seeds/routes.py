def seed_routes():
    db = SessionLocal()
    try:
        existing = db.query(Route).count()
        if existing > 0:
            print(f"Routes already seeded ({existing} rows). Skipping.")
            return

        rows = []
        for origin, destination, distance_km, duration_min, avg_price_usd in ROUTES:
            rows.append(Route(
                origin_iata=origin,
                destination_iata=destination,
                distance_km=distance_km,
                avg_duration_min=duration_min,
                avg_price_usd=avg_price_usd,
            ))
        db.bulk_save_objects(rows)
        db.commit()
        print(f"✓ Seeded {len(rows)} routes successfully.")
    finally:
        db.close()


if __name__ == "__main__":
    seed_routes()